import java.util.Scanner;

public class W4Q1 {

	public static void main(String[] args) {
	
		Scanner input = new Scanner(System.in);
		int x = input.nextInt();
		
		int i = 1;
		int sum = 0;
		
		while(i<=x);{
		  sum = sum + i;
		  i++;
		}
		
		System.out.println(sum);
		
		

}

}
